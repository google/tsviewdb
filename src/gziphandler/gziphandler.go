package gziphandler

import (
	"bytes"
	"compress/gzip"
	"io"
	"net/http"
	"strings"
)

type gzipResponseWriter struct {
	io.Writer
	http.ResponseWriter
}

func (m *gzipResponseWriter) Write(b []byte) (int, error) {
	return m.Writer.Write(b)
}

func NewGZipHandler(h http.Handler) http.Handler {
	return http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		if !strings.Contains(r.Header.Get("Accept-Encoding"), "gzip") {
			h.ServeHTTP(w, r)
			return
		}
		w.Header().Set("Content-Encoding", "gzip")
		gz := gzip.NewWriter(w)
		defer gz.Close()
		h.ServeHTTP(&gzipResponseWriter{Writer: gz, ResponseWriter: w}, r)
	})
}

func GzipContent(gzbuff *bytes.Buffer, uncompressedContent []byte) (err error) {
	gz := gzip.NewWriter(gzbuff)
	_, err = gz.Write(uncompressedContent)
	gz.Close() // TODO: handle the close error value.
	if err != nil {
		return err
	}
	return nil
}
